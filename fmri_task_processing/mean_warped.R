library(RNifti)
img = readNifti("warfmri.nii.gz")
mn = apply(img, 1:3, mean)
dim(mn)
class(mn)
dim(mn)
class(mn)
mn_img = updateNifti(image = mn, template = dumpNifti(img))
writeNifti(mn_img, file = "warfmri_mean.nii.gz")


